// # Keyboard Inputs
// (That we care about)
export enum Keys {
  i = "i", // Instance
  p = "p", // Port
  w = "w", // Wire
  r = "r", // Rotate
  h = "h", // Horizontal flip
  v = "v", // Vertical flip
  z = "z", // Undo / redo
  Comma = ",", // Save(?)
  Escape = "Escape", // Cancel
  Backspace = "Backspace", // Remove
  Delete = "Delete", // Remove
  Enter = "Enter", // Finish
  Space = " ", // Filter this out of names
}
